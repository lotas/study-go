package lib

import (
	"image"
	"image/color"
	"math/cmplx"
)

// Mandelbrot generate mandelbrot image
func Mandelbrot(width, height int, x, y float64) image.Image {
	var xmin, ymin, xmax, ymax float64 = x - 2, y - 2, x + 2, y + 2

	img := image.NewRGBA(image.Rect(0, 0, width, height))
	for py := 0; py < height; py++ {
		y := float64(py)/float64(height)*(ymax-ymin) + ymin
		for px := 0; px < width; px++ {
			x := float64(px)/float64(width)*(xmax-xmin) + xmin
			z := complex(x, y)

			// Image point (px, py) represents complex value z.
			img.Set(px, py, mandelbrot(z))
		}
	}
	return img
	// png.Encode(os.Stdout, img) // NOTE: ignoring errors
}

func mandelbrot(z complex128) color.Color {
	const iterations = 200
	const contrast = 15

	var v complex128
	for n := uint8(0); n < iterations; n++ {
		v = v*v + z
		if cmplx.Abs(v) > 2 {
			return color.RGBA{contrast * n, 0xFF - 3*contrast*n, 0xFF - 2*contrast*n, 0xFF}
			// return color.RGBA(0, 0, 0, 1)
			return color.Gray{255 - contrast*n}
		}
	}
	return color.Black
}

